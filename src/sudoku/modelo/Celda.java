package sudoku.modelo;

/**
 * Clase de celda
 * @author Félix Laguna Teno
 *
 */
public class Celda {
	/**
	 * Numero.
	 */
	private int numero;
	/**
	 * Fila.
	 */
	private int fila;
	/**
	 * Columna.
	 */
	private int columna;
	/**
	 * Constructor de celda.
	 * @param fila fila
	 * @param columna columna
	 */
	public Celda(int fila, int columna){
		this.fila=fila;
		this.columna=columna;
	}
	/**
	 * Coloca un número en la celda.
	 * @param numero numero
	 */
	public void establecerNumero(int numero){
		this.numero=numero;
	}
	/**
	 * Devuelve el número.
	 * @return número
	 */
	public int devolverNum(){
		return numero;
	}
	/**
	 * Devuelve si la celda tiene o no una letra.
	 * @return true si está vacía, false si no
	 */
	public boolean estaVacia(){
		return numero==0;
	}
	/**
	 * Consulta la fila de la celda.
	 * @return fila
	 */
	public int obtenerFila(){
		return fila;
	}
	/**
	 * Consulta la columna de la celda.
	 * @return columna
	 */
	public int obtenerColumna(){
		return columna;
	}
	/**
	 * Devuelve la letra como string.
	 * @return String de la letra
	 */
	public String obtenerString(){
		String s=new String();
		if (numero==0){
			s+="";
		}else{
			s+=numero;
		}
		return s;
		}
	/**
	 * Vacía la celda.
	 */
	public void vaciar(){
		numero=0;
	}
	@Override
	/**
	 * Devuelve la celda como String.
	 * @return cadena de la celda
	 */
	public String toString(){
		String s=new String();
		s+="<"+fila+"-"+numero+"-"+columna+">";
		return s;
	}
	/**
	 * Sobreescritura de clone.
	 */
	@Override
	public Celda clone(){
		Celda c=new Celda(fila,columna);
		c.establecerNumero(numero);
		return c;
	}
	/**
	 * Incrementa el número de la celda.
	 */
	public void incrementar(){
		if (numero>=9){
			numero=1;
		}else{
			numero++;
		}
	}
	/**
	 * Decrementa el número de la celda.
	 */
	public void decrementar(){
		if (numero<=1){
			numero=9;
		}else{
			numero--;
		}
	}
}
